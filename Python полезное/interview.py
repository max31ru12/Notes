# Глубокая и поверхностная копия
some_list = [1, [2], 3]

# поверхностная копия внешнего объекта получит новый адрес в памяти, а все, что внутри будет
# скопировано с сохранением ссылок на адрес памяти
import copy

print(some_list is copy.copy(some_list)) # False
print(some_list[1] is copy.copy(some_list)[1]) # True

