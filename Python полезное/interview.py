# self - это просто ссылка на какой-то уникальный объект созданного экземпляра класса
# super() служит для вызова родительских методов


class A:

    def __init__(self):
        self.__title = 'Some class'
        self._content = 'Some content'

    # Метод объекта с обязательным словом self
    # характеризуют поведение конкретного экземпляра какой-то сущности
    def a(self):
        pass

    # Методы, которые принадлежат классу (cls - ссылка на класс)
    @classmethod
    def b(cls):
        pass

    # Нет обязательных аргументов (не принадлежит ни классу, ни объекту, помещен в класс по смыслу)
    @staticmethod
    def c():
        pass


ex = A()

print(ex._content)
print(ex._A__title)


